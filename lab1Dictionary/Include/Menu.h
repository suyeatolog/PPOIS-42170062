/*! \file Menu.h
*  \brief Заголовочный файл с описанием класса Menu
*  \details Содержит объявление класса Menu,
*           который реализует пользовательский интерфейс
*           для работы с англо-русским словарем через консоль.
*/
#ifndef LABWORK1_MENU_H
#define LABWORK1_MENU_H
#include "Dictionary.h"

/*! \class Menu
 *  \brief Класс, реализующий консольное меню для работы со словарем
 *  \details Предоставляет интерактивный текстовый интерфейс для выполнения
 *           всех основных операций со словарем: добавление, удаление, поиск,
 *           изменение слов и загрузка из файла.
 */
class Menu {
public:

    /*! \brief Основной метод запуска меню
     *  \details Запускает главный цикл программы, отображает меню
     *           и обрабатывает пользовательский ввод до выхода из программы.
     *           Настраивает кодировку консоли для корректного отображения
     *           русских символов.
     */
    void RunMenu();
private:

    /*! \brief Объект словаря для работы
     *  \details Основной объект Dictionary, с которым производятся
     *           все операции через меню
     */
    Dictionary Dictionary;

    /*! \brief Метод отображения главного меню
     *  \details Выводит в консоль красиво оформленное меню со всеми
     *           доступными операциями со словарем
     */
    void PrintMenu();

    /*! \brief Метод добавления нового слова в словарь
     *  \details Запрашивает у пользователя английское слово и русский перевод,
     *           проверяет наличие слова в словаре и добавляет новую пару
     */
    void AddWord();

    /*! \brief Метод удаления слова из словаря
     *  \details Запрашивает у пользователя английское слово и удаляет его
     *           из словаря вместе с переводом
     */
    void DeleteWord();

    /*! \brief Метод изменения перевода слова
     *  \details Запрашивает английское слово и новый русский перевод,
     *           обновляет перевод существующего слова
     */
    void ChangeTranslation();

    /*! \brief Метод вывода количества слов в словаре
     *  \details Отображает текущее количество слов, хранящихся в словаре
     */
    void PrintWordCount();

    /*! \brief Метод перевода слова
     *  \details Запрашивает английское слово и выводит его русский перевод
     *           или сообщение об отсутствии слова в словаре
     */
    void TranslateWord();

    /*! \brief Метод вывода всего словаря
     *  \details Отображает все слова словаря в алфавитном порядке
     *           с их переводами
     */
    void PrintDictionary();

    /*! \brief Метод загрузки словаря из файла
     *  \details Загружает словарь из текстового файла, предварительно
     *           очищая текущий словарь. Использует класс ReadFile
     *           для чтения данных из файла.
     */
    void LoadDictionaryFromFile();
};

#endif //LABWORK1_MENU_H